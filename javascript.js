// for the function playRound we added two paramters playerSelection(generated by the input of the player) & computerSelection(is generated by the computerPlay() function we had created above). 

function playRound(playerSelection, computerSelection) {
   if (playerSelection === computerSelection) {
      console.log(`It's a tie! you both picked ${playerSelection}`);
      return "tie";
   } else if (playerSelection !== "rock" && playerSelection !== "paper" && playerSelection !== "scissors"){
      alert(`You sure about using ${playerSelection} on a game of "Rock, paper`)
   } else if (playerSelection === "rock" && computerSelection === "scissors") {
      console.log(`You win! ${playerSelection} beats ${computerSelection}`);
      return "playerWin";
   } else if (playerSelection === "paper" && computerSelection === "rock") {
      console.log(`You win! ${playerSelection} beats ${computerSelection}`);
      return "playerWin";
   } else if (playerSelection === "scissors" && computerSelection === "paper") {
      console.log(`You win! ${playerSelection} beats ${computerSelection}`);
      return "playerWin";
   } else {
      console.log(`You lose! ${computerSelection} beats ${playerSelection}`);
      return "botWin";
   }
}

const computerSelection = computerPlay();



function game() {
   let playerScore = 0;
   let botScore = 0;
   let gameWinner = '';
   
   for (let i = 0; i < 5; i++) {

      let playerSelection = prompt(`Round ${i+1}: Choose among "Rock, Paper, Scissors" as your weapon`).toLowerCase();
      let roundResult = playRound(playerSelection, computerPlay());

      if (roundResult === "playerWin" ) {
         playerScore++;
      } else if (roundResult === "botWin" ) {
         botScore++;
      }
   }

   if (playerScore > botScore) {
      gameWinner = 'You win!';
   } else if (botScore > playerScore) {
      gameWinner = 'You lose, Computer wins!';
   } else {
      gameWinner = 'Draw';
   }

   console.log(`Player: ${playerScore} | Bot: ${botScore}`);

   if (gameWinner === 'Draw') {
      console.log("There is no match winner, draw!");
   } else {
      console.log(`${gameWinner}`);
   }
}

game();